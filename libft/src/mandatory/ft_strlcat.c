#include "libft.h"

size_t	ft_strlcat(char *dest, const char *src, size_t dstsize)
{
	char	*d;
	char	*s;
	size_t	n;
	size_t	dlen;

	d = (char *)dest;
	s = (char *)src;
	n = dstsize;
	while (n-- && *d)
		d++;
	dlen = d - dest;
	n = dstsize - dlen;
	if (n == 0)
		return (dlen + ft_strlen(s));
	while (*s)
	{
		if (n != 1)
		{
			*d++ = *s;
			n--;
		}
		s++;
	}
	*d = '\0';
	return (dlen + (s - src));
}
